package com.itplus.entity;

public class FloatValueObject {
	private float value;
	public FloatValueObject() {
		super();
	}
	public FloatValueObject(float value) {
		super();
		this.value = value;
	}

	public float getValue() {
		return value;
	}

	public void setValue(float value) {
		this.value = value;
	}
	
}
